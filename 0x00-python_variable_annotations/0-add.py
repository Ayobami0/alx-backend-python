#!/usr/bin/env python3
"""
Contains a function add that takes a float a and a float b
as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """Adds to floats and returns their result.

    Args:
        a (float): a number
        b (float): another number

    Returns:
        float: The sum of the addition of both numbers
    """
    return a + b
